def fun(_dividend, _divisor, _count, _res):
    print("Dividend = ", _dividend)
    print("Divisor  = ", _divisor)
    print("Count    = ", _count)
    counter = 0
    while (_dividend > _divisor):
        _dividend = _dividend - _divisor
        counter = counter + 1

    if (counter != _count):
        print ("Counter error, Expected = ", _count, ", Got = ", counter)
    if (_dividend != _res):
        print ("Dividend error, Expected = ", _dividend, ", Got = ", _res)
    print("\n\n")
       
  
